public class InternationalizationTest {

    public InternationalizationTest() {
    }

    @BeforeClass
    public static void setUpClass() {
    }

    @AfterClass
    public static void tearDownClass() {
    }

    @Before
    public void setUp() {
    }

    @After
    public void tearDown() {
    }

    @Test
    public void help() {
        assertEquals("/help/jacksum/help_en.txt",
                ResourceBundle.getBundle("resources", Locale.US).getString("help_file"));
        
        assertEquals("/help/jacksum/help_en.txt",
                ResourceBundle.getBundle("resources", Locale.CHINA).getString("help_file"));
        
        assertEquals("/help/jacksum/help_de.txt",
                ResourceBundle.getBundle("resources", Locale.GERMANY).getString("help_file"));

    }
}
